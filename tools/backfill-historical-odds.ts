/**
 * backfill-historical-odds.ts — Fetches and caches historical odds from The Odds API
 * for all historical backtest snapshots (2023 onward).
 *
 * Cache location: cache/historical-odds/{sport_key}/{YYYY-MM-DD}.json
 * Each file contains the full API response for that sport_key at {date}T00:00:00Z,
 * which includes pre-match odds for all games scheduled that day.
 *
 * Resume-safe: skips dates that already have a cached file.
 *
 * Prerequisite: backtest snapshot files must exist in cache/predictions/ for the league.
 *   Run scripts/run-backtest-v3-historical.ts first if they don't exist.
 *   For AF canonical leagues (CL, AR, BR, MX): run-backtest-v3-historical.ts reads from
 *   cache/historical/apifootball/{leagueId}/{year}.json (generated by calibrate-league-report.ts).
 *
 * This tool is called automatically by the /calibrate-league skill (Step 3.5) after
 * calibration is wired. It can also be run manually at any time — it is resume-safe.
 *
 * Usage:
 *   npx tsx --tsconfig tsconfig.server.json tools/backfill-historical-odds.ts
 *   npx tsx --tsconfig tsconfig.server.json tools/backfill-historical-odds.ts --dry-run
 *   npx tsx --tsconfig tsconfig.server.json tools/backfill-historical-odds.ts --comp PD
 *   npx tsx --tsconfig tsconfig.server.json tools/backfill-historical-odds.ts --sync-ledger
 *
 * --comp SLUG: Restrict to a single competition (slug or numeric leagueId).
 *             Requires cache/predictions/historical-backtest-v3-{slug}-*.json to exist.
 * --sync-ledger: Seeds the OPS panel ledger with the most recent remaining value from
 *   the cache files. Run once after a backfill that predates ledger integration.
 *
 * The Odds API sport_key mapping for each league lives in COMP_CODE_TO_SPORT_KEY below.
 * To add a new league: add an entry here AND in server/prediction/historical-odds-store.ts.
 */

import * as crypto from 'node:crypto';
import * as fs from 'node:fs';
import * as path from 'node:path';
import { config } from 'dotenv';
import { ApiUsageLedger } from '@sportpulse/canonical';

config();

// ── Config ──────────────────────────────────────────────────────────────────

const CACHE_DIR = path.resolve(process.cwd(), 'cache/historical-odds');
const BACKTEST_DIR = path.resolve(process.cwd(), 'cache/predictions');
const API_KEY = process.env.THE_ODDS_API_KEY ?? '';
const DELAY_MS = 1200; // ~50 req/min — well within paid plan limits
const MIN_YEAR = 2023;

// ── Competition code → sport_key ────────────────────────────────────────────

